// ---------------------------------------------------------------------------
// Hex encoding helper
// ---------------------------------------------------------------------------
static void hex_encode(const unsigned char* data, size_t len, char* out) {
    static const char hex[] = "0123456789abcdef";
    for (size_t i = 0; i < len; i++) {
        out[i*2]   = hex[(data[i] >> 4) & 0x0F];
        out[i*2+1] = hex[data[i] & 0x0F];
    }
    out[len*2] = '\0';
}

// ---------------------------------------------------------------------------
// Generate MAC for a data string using CommonCrypto
// ---------------------------------------------------------------------------
static void generateMAC(const char* data, char macOutput[65]) {
    if (g_integrity_token != 0x000000) {
        NativeSecrets_secureExit();
    }

    unsigned char key[32];
    deobfuscate(HMAC_KEY_OBFUSCATED, HMAC_KEY_MASK_A, HMAC_KEY_MASK_B,
                32, key, HMAC_KEY_COEF1, HMAC_KEY_COEF2);

    unsigned char mac[CC_SHA256_DIGEST_LENGTH];
    CCHmac(kCCHmacAlgSHA256, key, 32, data, strlen(data), mac);
    hex_encode(mac, CC_SHA256_DIGEST_LENGTH, macOutput);

    secureZero(key, 32);
    secureZero(mac, CC_SHA256_DIGEST_LENGTH);
}

// ---------------------------------------------------------------------------
// Inline hook detection (architecture-guarded)
// ---------------------------------------------------------------------------
static int checkInlineHooks(void) {
#if defined(__aarch64__)
    void* funcAddr = (void*)checkInlineHooks;
    unsigned char* bytes = (unsigned char*)funcAddr;

    // ARM64: LDR X16, #8; BR X16 = Frida/Substrate trampoline pattern
    if (bytes[0] == 0x50 && bytes[1] == 0x00 &&
        bytes[2] == 0x00 && bytes[3] == 0x58) {
        return 1;
    }

    // ARM64: ADRP + BR X16/X17 — another common trampoline
    // Check for BR X16 (0xD61F0200) or BR X17 (0xD61F0220)
    uint32_t insn;
    memcpy(&insn, bytes + 4, 4);
    if (insn == 0xD61F0200 || insn == 0xD61F0220) {
        return 1;
    }

    return 0;
#elif defined(__arm__)
    void* funcAddr = (void*)checkInlineHooks;
    unsigned char* bytes = (unsigned char*)funcAddr;
    if (bytes[3] == 0xE5 && bytes[2] == 0x9F && bytes[1] == 0xF0) {
        return 1;
    }
    return 0;
#else
    // x86/x86_64 (simulators) — skip hook detection
    return 0;
#endif
}

// ---------------------------------------------------------------------------
// Free a result string
// ---------------------------------------------------------------------------
void NativeSecrets_freeResult(char* result) {
    if (result) {
        size_t len = strlen(result);
        volatile char* p = (volatile char*)result;
        for (size_t i = 0; i < len; i++) { p[i] = 0; }
        free(result);
    }
}

// ---------------------------------------------------------------------------
// Sign a security result
// Returns: malloc'd "STATUS:details:timestamp:hmac_hex"
// ---------------------------------------------------------------------------
char* NativeSecrets_signSecurityResult(const char* status) {
    if (g_integrity_token != 0x000000) {
        NativeSecrets_secureExit();
    }

    char payload[512];
    long timestamp = (long)time(NULL);
    snprintf(payload, sizeof(payload), "%s:%ld", status, timestamp);

    char mac[65];
    generateMAC(payload, mac);

    size_t resultLen = strlen(payload) + 1 + 64 + 1;
    char* result = (char*)malloc(resultLen);
    if (!result) return NULL;
    snprintf(result, resultLen, "%s:%s", payload, mac);

    return result;
}

// ---------------------------------------------------------------------------
// Enforce a signed security result (crashes on failure)
// ---------------------------------------------------------------------------
void NativeSecrets_enforceSecurityResult(const char* signedResult) {
    if (g_integrity_token != 0x000000) {
        NativeSecrets_secureExit();
    }

    // Parse: find last ':' which separates the HMAC from the payload
    const char* lastColon = strrchr(signedResult, ':');
    if (!lastColon || lastColon == signedResult) {
        NativeSecrets_secureExit();
    }

    size_t payloadLen = (size_t)(lastColon - signedResult);
    char payload[512];
    if (payloadLen >= sizeof(payload)) {
        NativeSecrets_secureExit();
    }
    memcpy(payload, signedResult, payloadLen);
    payload[payloadLen] = '\0';

    const char* providedMAC = lastColon + 1;

    char expectedMAC[65];
    generateMAC(payload, expectedMAC);

    // Constant-time comparison
    int diff = 0;
    if (strlen(providedMAC) != 64) {
        diff = 1;
    } else {
        for (int i = 0; i < 64; i++) {
            diff |= providedMAC[i] ^ expectedMAC[i];
        }
    }

    // Check timestamp freshness (< 30 seconds)
    if (diff == 0) {
        const char* tsColon = strrchr(payload, ':');
        if (tsColon) {
            long ts = atol(tsColon + 1);
            long now = (long)time(NULL);
            if (now - ts > 30 || ts - now > 5) {
                diff = 1;
            }
        } else {
            diff = 1;
        }
    }

    secureZero(payload, sizeof(payload));
    secureZero(expectedMAC, 65);

    if (diff != 0) {
        NativeSecrets_secureExit();
    }

    if (checkInlineHooks()) {
        NativeSecrets_secureExit();
    }
}

// ---------------------------------------------------------------------------
// Verify certificate pin — returns malloc'd signed result
// ---------------------------------------------------------------------------
char* NativeSecrets_verifyCertificatePin(const char* spkiHash) {
    if (g_integrity_token != 0x000000) {
        NativeSecrets_secureExit();
    }

    const char* status;
    if (strcmp(spkiHash, CERT_PIN_LEAF) == 0 ||
        strcmp(spkiHash, CERT_PIN_INTERMEDIATE) == 0 ||
        strcmp(spkiHash, CERT_PIN_ROOT) == 0) {
        status = "VALID";
    } else {
        status = "INVALID";
    }

    char payload[256];
    long timestamp = (long)time(NULL);
    snprintf(payload, sizeof(payload), "%s:%ld", status, timestamp);

    char mac[65];
    generateMAC(payload, mac);

    size_t resultLen = strlen(payload) + 1 + 64 + 1;
    char* result = (char*)malloc(resultLen);
    if (!result) return NULL;
    snprintf(result, resultLen, "%s:%s", payload, mac);

    return result;
}

// ---------------------------------------------------------------------------
// Get deobfuscated endpoint URL — returns malloc'd string
// ---------------------------------------------------------------------------
char* NativeSecrets_getEndpoint(const char* key) {
    if (g_integrity_token != 0x000000) {
        NativeSecrets_secureExit();
    }

    if (strcmp(key, "relay") == 0) {
        size_t len = sizeof(ENDPOINT_RELAY_OBFUSCATED);
        unsigned char deobfuscated[256];
        deobfuscate(ENDPOINT_RELAY_OBFUSCATED, ENDPOINT_RELAY_MASK_A,
                     ENDPOINT_RELAY_MASK_B, len, deobfuscated,
                     ENDPOINT_RELAY_COEF1, ENDPOINT_RELAY_COEF2);

        char* result = (char*)malloc(len + 1);
        if (!result) {
            secureZero(deobfuscated, sizeof(deobfuscated));
            return NULL;
        }
        memcpy(result, deobfuscated, len);
        result[len] = '\0';
        secureZero(deobfuscated, sizeof(deobfuscated));
        return result;
    }

    return NULL;
}
